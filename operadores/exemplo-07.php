<?php
    // Cria a variável $a
    $a = 10;
    // Aqui ele imprime e depois incrementa
    echo $a++;
    echo "</br>";
    // Exibe o valor já incrementado
    echo $a;
    echo "</br>";
    // Cria a variável $b
    $b = 10;
    // Aqui ele incrementa e depois imprime
    echo ++$b;
    echo "</br>";
    // Aqui ele repete o valor anterior que já estava incrementado
    echo $b;
    echo "</br>";
    // Podemos utilizar o operador de decremento também
    // $a valia 11 anteriormente então utilizamos o operador de decremento
    $a--;
    // Imprimimos o valor já decrementado
    echo $a;
?>